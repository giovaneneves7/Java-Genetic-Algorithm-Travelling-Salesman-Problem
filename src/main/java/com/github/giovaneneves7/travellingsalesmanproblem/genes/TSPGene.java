package com.github.giovaneneves7.travellingsalesmanproblem.genes;

/**
 * TSPGene's class
 * @author Giovane Neves
 */
public class TSPGene {
    
    private final int x;
    private final int y;

    public TSPGene(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
}
